int		ft_atoi(char *str)
{
	int i;
	int a;
	int nb;

	nb = 0;
	i = 0;
	a = 0;
	while (str[i] <= ' ')
		i++;
	if (str[i] == '-')
		a = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while ((str[i] != '\0') && (str[i] >= '0') && (str[i] <= '9'))
	{
		nb = nb * 10 + str[i] - '0';
		i++;
	}
	if (a == 1)
		return (-nb);
	else
		return (nb);
}
