#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int c;

	c = *a;
	*a = *b;
	*b = c;
}

int main()
{
	int n;
	int m;

	n = 32;
	m = 55;
	printf("%d  %d",n ,m);
	ft_swap(&n, &m);
	printf("\n");
	printf("%d  %d",n ,m);
	return (0);
}
